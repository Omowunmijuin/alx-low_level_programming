#include <stdio.h>

/**
 * _isupper - checks for uppercase character
 * Description: check the cade for a digit
 * @c: character to be checked
 * Return: 1 if c is uppercase, otherwise return 0
*/

int _isupper(int c);
{

	if (c >= '0' && c <= '9')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
